def _sanitize_filename(value: str) -> str:
    """Safe filename component. No illegal chars."""
    return re.sub(r'[^a-zA-Z0-9_\-]', '_', value)[:30]


def _sanitize_sheet_name(value: str) -> str:
    """
    Sanitize Excel sheet name.
    Excel forbids: \\ / ? * [ ] :
    Max 31 characters.
    """
    sanitized = re.sub(r'[\\/?*\[\]:]', ' ', value)
    return sanitized[:31].strip()


def _load_kb_file(path: Path) -> str:
    """Load KB file. Returns empty string on failure."""
    try:
        if path.exists():
            with open(path, encoding="utf-8") as f:
                return f.read()
    except Exception as e:
        console.print(
            f"[yellow]  KB load failed ({path.name}): "
            f"{e}[/yellow]"
        )
    return ""


def _get_due_date(severity_label: str) -> str:
    """Calculate due date from severity label."""
    days = DUE_DATE_MAP.get(severity_label, 180)
    due  = datetime.now() + timedelta(days=days)
    return due.strftime("%Y-%m-%d")


def _get_framework_controls(
    finding_type: str,
    frameworks:   list[str]
) -> dict:
    """
    Get all applicable controls for a finding
    across all loaded frameworks.
    Returns dict of framework display name -> controls list.
    """
    controls = {}

    if "nist_800_53" in frameworks:
        controls["NIST 800-53"] = NIST_MAPPING.get(
            finding_type, NIST_MAPPING["default"]
        )
    if "nerc_cip" in frameworks:
        controls["NERC CIP"] = NERC_CIP_MAPPING.get(
            finding_type, NERC_CIP_MAPPING["default"]
        )
    if "cis_controls" in frameworks:
        controls["CIS Controls"] = CIS_MAPPING.get(
            finding_type, CIS_MAPPING["default"]
        )
    if "iso_27001" in frameworks:
        controls["ISO 27001"] = ISO_MAPPING.get(
            finding_type, ISO_MAPPING["default"]
        )
    if "nist_ai_rmf" in frameworks:
        controls["NIST AI RMF"] = AI_RMF_MAPPING.get(
            finding_type, AI_RMF_MAPPING["default"]
        )

    return controls


