class GRCMapper:
    """
    GRC compliance mapping module.

    Takes aggregated findings and maps them to compliance
    frameworks based on organization type.

    Org type selected once and saved to OSINTProfile.
    AI RMF loaded automatically if AI surfaces found.

    Each output independent -- one failure never stops others.
    Excel sheet names sanitized -- no illegal characters.
    """

    def __init__(
        self,
        target: str,
        mode:   str = "SEMI-AUTO",
    ):
        self.target     = target
        self.mode       = mode.upper()
        self.org_type   = None
        self.frameworks: list[str] = []
        REPORTS_DIR.mkdir(parents=True, exist_ok=True)

    def select_org_type(
        self, saved_org_type: Optional[str] = None
    ) -> str:
        """
        Show org type selection menu.
        Skips menu if org_type already saved to profile.
        while True loop -- exits only on valid input 1-5.
        """
        if saved_org_type:
            console.print(
                f"[dim]  Organization type: "
                f"{saved_org_type} "
                f"(from profile)[/dim]"
            )
            return saved_org_type

        console.print(
            Panel(
                "[bold]Select your organization type:"
                "[/bold]\n\n"
                "[cyan][1][/cyan] Personal / Researcher\n"
                "    CIS Controls + AI RMF (if AI found)\n\n"
                "[cyan][2][/cyan] Small Business\n"
                "    CIS Controls + NIST 800-53\n\n"
                "[cyan][3][/cyan] Enterprise\n"
                "    NIST 800-53 + ISO 27001\n\n"
                "[cyan][4][/cyan] Critical Infrastructure\n"
                "    NERC CIP + NIST 800-53 + AI RMF\n\n"
                "[cyan][5][/cyan] All Frameworks\n"
                "    Everything above",
                title=(
                    "[bold cyan]"
                    "R3D -- Organization Profile"
                    "[/bold cyan]"
                ),
                border_style="cyan"
            )
        )

        org_map = {
            "1": "personal",
            "2": "small_business",
            "3": "enterprise",
            "4": "critical_infrastructure",
            "5": "all",
        }

        while True:
            try:
                choice = input("\nSelect [1-5]: ").strip()
            except EOFError:
                # Non-interactive mode (stdin piped/exhausted) --
                # default to Personal/Researcher
                console.print(
                    "[yellow]  Non-interactive mode — "
                    "defaulting to Personal/Researcher[/yellow]\n"
                    "[dim]  Tip: pass --org-type to skip this "
                    "menu (e.g. --org-type enterprise)[/dim]"
                )
                return "personal"
            if choice in org_map:
                org_type = org_map[choice]
                console.print(
                    f"[green]  Selected: "
                    f"{org_type.replace('_', ' ').title()}"
                    f"[/green]"
                )
                return org_type
            console.print(
                "[yellow]  Invalid. Enter 1, 2, 3, 4, "
                "or 5.[/yellow]"
            )

    def load_frameworks(
        self,
        org_type: str,
        has_ai:   bool = False
    ) -> list[str]:
        """
        Load relevant frameworks for org type.
        AI RMF added automatically if AI surfaces found.
        Skips frameworks with missing KB files.
        """
        frameworks = list(
            ORG_FRAMEWORK_MAP.get(org_type, ["cis_controls"])
        )

        # AI RMF auto-loads if AI surfaces found
        if has_ai and "nist_ai_rmf" not in frameworks:
            frameworks.append("nist_ai_rmf")
            console.print(
                "[dim]  AI surfaces detected -- "
                "NIST AI RMF added[/dim]"
            )

        # Only load frameworks with KB files on disk
        available = []
        for fw in frameworks:
            kb_path = FRAMEWORK_KB_MAP.get(fw)
            if kb_path and kb_path.exists():
                available.append(fw)
                console.print(
                    f"[dim]  Loaded: "
                    f"{FRAMEWORK_NAMES.get(fw, fw)}[/dim]"
                )
            else:
                console.print(
                    f"[yellow]  KB missing: {fw} -- "
                    f"skipping[/yellow]"
                )

        return available

    # ------------------------------------------------------------------ #
    # OUTPUT 1 -- RISK REGISTER
    # ------------------------------------------------------------------ #

    def generate_risk_register(
        self,
        aggregated: AggregatedFindings,
        frameworks: list[str]
    ) -> Optional[str]:
        """
        Generate risk register XLSX.
        One row per finding with all framework controls,
        calculated due dates, owner and status fields.
        Full try/except -- returns None on failure.
        """
        console.print(
            "[cyan]  Generating risk register...[/cyan]"
        )

        try:
            rows = []
            for finding in aggregated.findings:
                controls = _get_framework_controls(
                    finding.finding_type, frameworks
                )

                rows.append({
                    "Finding ID":    finding.id,
                    "Title":         finding.title,
                    "Severity":      finding.severity_label,
                    "Score":         finding.severity_score,
                    "Module":        finding.source_module,
                    "Finding Type":  finding.finding_type,
                    "Description":   finding.description,
                    "MITRE":         finding.mitre_technique,
                    "MITRE Name":    finding.mitre_technique_name,
                    "OWASP":         finding.owasp_category,
                    "OWASP Name":    finding.owasp_category_name,
                    "NIST 800-53":   " · ".join(
                        controls.get("NIST 800-53", [])
                    ),
                    "NERC CIP":      " · ".join(
                        controls.get("NERC CIP", [])
                    ),
                    "CIS Controls":  " · ".join(
                        controls.get("CIS Controls", [])
                    ),
                    "ISO 27001":     " · ".join(
                        controls.get("ISO 27001", [])
                    ),
                    "NIST AI RMF":   " · ".join(
                        controls.get("NIST AI RMF", [])
                    ),
                    "CVE ID":        finding.cve_id or "",
                    "CVSS Score":    finding.cvss_score or "",
                    "Zero Day":      (
                        "YES" if finding.zero_day_flag
                        else "NO"
                    ),
                    "Due Date":      _get_due_date(
                        finding.severity_label
                    ),
                    "Owner":         "TBD",
                    "Status":        "Open",
                    "Target":        finding.target,
                    "Timestamp":     finding.timestamp,
                })

            df = pd.DataFrame(rows)

            safe_target = _sanitize_filename(self.target)
            timestamp   = datetime.now().strftime(
                "%Y-%m-%d_%H-%M-%S"
            )
            xlsx_path = (
                REPORTS_DIR /
                f"{timestamp}_{safe_target}"
                f"_risk_register.xlsx"
            )

            with pd.ExcelWriter(
                str(xlsx_path), engine="openpyxl"
            ) as writer:
                df.to_excel(
                    writer,
                    index=False,
                    sheet_name="Risk Register"
                )
                ws = writer.sheets["Risk Register"]
                for col in ws.columns:
                    max_len = max(
                        len(str(cell.value))
                        if cell.value is not None else 0
                        for cell in col
                    )
                    ws.column_dimensions[
                        col[0].column_letter
                    ].width = min(max_len + 4, 60)

            console.print(
                f"[green]  Risk register: "
                f"{xlsx_path}[/green]"
            )
            return str(xlsx_path)

        except Exception as e:
            console.print(
                f"[red]  Risk register failed: {e}[/red]"
            )
            return None

    # ------------------------------------------------------------------ #
    # OUTPUT 2 -- COMPLIANCE MAP
    # ------------------------------------------------------------------ #

    def generate_compliance_map(
        self,
        aggregated: AggregatedFindings,
        frameworks: list[str]
    ) -> Optional[str]:
        """
        Generate compliance map XLSX.
        One sheet per framework.
        Sheet names sanitized -- no illegal Excel chars.
        Full try/except -- returns None on failure.
        """
        console.print(
            "[cyan]  Generating compliance map...[/cyan]"
        )

        try:
            safe_target = _sanitize_filename(self.target)
            timestamp   = datetime.now().strftime(
                "%Y-%m-%d_%H-%M-%S"
            )
            xlsx_path = (
                REPORTS_DIR /
                f"{timestamp}_{safe_target}"
                f"_compliance_map.xlsx"
            )

            with pd.ExcelWriter(
                str(xlsx_path), engine="openpyxl"
            ) as writer:

                for framework in frameworks:
                    fw_name = FRAMEWORK_NAMES.get(
                        framework, framework
                    )
                    rows = []

                    for finding in aggregated.findings:
                        controls = _get_framework_controls(
                            finding.finding_type, [framework]
                        )
                        fw_controls = list(
                            controls.values()
                        )
                        if not fw_controls:
                            continue

                        control_str = " · ".join(
                            fw_controls[0]
                            if fw_controls else []
                        )

                        gap_status = (
                            "Missing"
                            if finding.severity_label in [
                                "CRITICAL", "HIGH"
                            ]
                            else "Partial"
                            if finding.severity_label == "MEDIUM"
                            else "Needs Review"
                        )

                        rows.append({
                            "Control(s)":   control_str,
                            "Finding":      finding.title,
                            "Severity":     finding.severity_label,
                            "Score":        finding.severity_score,
                            "Gap Status":   gap_status,
                            "Type":         finding.finding_type,
                            "Description":  finding.description[:200],
                            "Due Date":     _get_due_date(
                                finding.severity_label
                            ),
                            "Remediation":  (
                                finding.remediation or
                                "See risk register"
                            ),
                        })

                    if not rows:
                        continue

                    df = pd.DataFrame(rows)

                    # Sanitize sheet name -- no illegal chars
                    sheet_name = _sanitize_sheet_name(
                        fw_name
                    )

                    df.to_excel(
                        writer,
                        index=False,
                        sheet_name=sheet_name
                    )

                    ws = writer.sheets[sheet_name]
                    for col in ws.columns:
                        max_len = max(
                            len(str(cell.value))
                            if cell.value is not None else 0
                            for cell in col
                        )
                        ws.column_dimensions[
                            col[0].column_letter
                        ].width = min(max_len + 4, 50)

            console.print(
                f"[green]  Compliance map: "
                f"{xlsx_path}[/green]"
            )
            return str(xlsx_path)

        except Exception as e:
            console.print(
                f"[red]  Compliance map failed: "
                f"{e}[/red]"
            )
            return None

